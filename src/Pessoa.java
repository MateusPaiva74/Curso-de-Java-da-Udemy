public abstract class Pessoa {
    private String name;
    private int idade;

    public abstract int retornaInteiro();

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }
}
